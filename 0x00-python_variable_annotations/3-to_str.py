#!/usr/bin/env python3
"""
Function to_str which takes a float n as argument
and returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of a float.

    Args:
        n (float): The float to convert to a string.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
